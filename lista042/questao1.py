'''
1.	Fazer um algoritmo que peça um número, e ao final, informe se o número digitado está acima de 1000 ou abaixo de 1000.
'''

num = int(input("Me diga um número: "))

if (num >= 1000):
    print(f"O número {num} está acima de 1000.")
else:
    print(f"O número {num} está abaixo de 1000.")
print("FIM DO PROGRAMA.")